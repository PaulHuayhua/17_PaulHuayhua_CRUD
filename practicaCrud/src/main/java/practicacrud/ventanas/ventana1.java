    public void listar() {
        model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nombre");
        model.addColumn("Correo");
        model.addColumn("Rol");
        model.addColumn("Estado");

        tablaDatos.setModel(model);

        String sql = "SELECT * FROM usuario";

        try {
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] row = new Object[5];
                for (int i = 0; i < row.length; i++) {
                    row[i] = rs.getObject(i + 1);
                }
                model.addRow(row);
            }
            System.out.println("Los datos se mostraron correctamente");
        } catch (Exception e) {
            System.out.println("Error al intentar mostrar los datos: " + e.getMessage());
        }
    }

    public void guardar(String nombre, String correo, String rol, String estado) {
        String sql = "INSERT INTO usuario(nombre, correo, rol, estado) VALUES (?, ?, ?, ?)";
        try {
            cn = con.getConnection();
            ps = cn.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, correo);
            ps.setString(3, rol);
            ps.setString(4, estado);
            ps.executeUpdate();
            System.out.println("Datos agregados correctamente");
        } catch (Exception e) {
            System.out.println("Error al intentar agregar un nuevo dato: " + e.getMessage());
        }
    }

    public void modificar(String nombre, String correo, String rol, String estado, String id) {
        String sql = "UPDATE usuario SET nombre=?, correo=?, rol=?, estado=? WHERE identificador=?";

        Connection cn = null;
        PreparedStatement ps = null;

        try {
            cn = con.getConnection(); // Obtener la conexión
            ps = cn.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, correo);
            ps.setString(3, rol);
            ps.setString(4, estado);
            ps.setString(5, id);
            ps.executeUpdate();

            System.out.println("El usuario se modificado correctamente");

        } catch (Exception e) {
            System.out.println("Error al intentar modificar datos: " + e.getMessage());
        }
    }

    public void eliminar(String id) {
        String sql = "DELETE FROM usuario WHERE identificador=?";

        Connection cn = null;
        PreparedStatement ps = null;

        try {
            cn = con.getConnection();
            ps = cn.prepareStatement(sql);
            ps.setString(1, id);
            int res = ps.executeUpdate();
            System.out.println("Usuario eliminado correctamente");
        } catch (Exception e) {
            System.out.println("Error al eliminar un usuario: " + e.getMessage());
        }
    }

